  <h1>Onion Pakoda</h1>
  <img src="https://www.mygingergarlickitchen.com/wp-content/rich-markup-images/16x9/16x9-pyaaz-pakoda-onion-fritters.jpg" alt="Onion Pakoda">
  <p>Crispy and golden fried onion fritters, seasoned with spices, perfect as a snack or tea-time treat.</p>

  <h2>Ingredients</h2>
  <ul>
    <li>2 large onions, thinly sliced</li>
    <li>1 cup gram flour (besan)</li>
    <li>2 green chilies, finely chopped</li>
    <li>1 tsp cumin seeds</li>
    <li>½ tsp red chili powder</li>
    <li>½ tsp turmeric powder</li>
    <li>2 tbsp chopped coriander leaves</li>
    <li>Salt to taste</li>
    <li>Water as needed to make a thick batter</li>
    <li>Oil for deep frying</li>
  </ul>

  <h2>Instructions</h2>
  <ol>
    <li>In a large bowl, mix sliced onions, green chilies, cumin seeds, red chili powder, turmeric, coriander, and salt.</li>
    <li>Add gram flour gradually and mix well to coat the onions. Add a little water if needed to form a thick batter.</li>
    <li>Heat oil in a deep pan over medium heat.</li>
    <li>Drop small portions of the batter into hot oil and fry until golden brown and crispy.</li>
    <li>Remove pakodas using a slotted spoon and drain on paper towels.</li>
    <li>Serve hot with chutney or ketchup.</li>
  </ol>

  <h2>Tips</h2>
  <ul>
    <li>Make sure the batter is thick enough to hold the onions together while frying.</li>
    <li>Do not overcrowd the pan to maintain crispiness.</li>
    <li>Serve immediately for the best taste and texture.</li>
  </ul>

