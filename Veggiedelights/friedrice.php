    <h1>Vegetable Fried Rice</h1>
    <img src="https://www.sharmispassions.com/wp-content/uploads/2011/01/VegFriedRice2.jpg" alt="Vegetable Fried Rice">
    <p>A simple yet flavorful dish made with stir-fried rice, mixed vegetables, and soy sauce, perfect as a main or side dish.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>2 cups cooked rice (preferably cold)</li>
      <li>1/2 cup peas</li>
      <li>1/2 cup carrot, diced</li>
      <li>1/2 cup bell peppers, diced</li>
      <li>2 cloves garlic, minced</li>
      <li>2 tbsp soy sauce</li>
      <li>1 tbsp vegetable oil</li>
      <li>Salt and pepper to taste</li>
      <li>Chopped green onions (optional)</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Heat oil in a pan and sauté garlic until fragrant.</li>
      <li>Add peas, carrots, and bell peppers. Stir-fry 3–4 minutes until slightly tender.</li>
      <li>Add rice and soy sauce. Toss well to combine all ingredients.</li>
      <li>Season with salt and pepper, garnish with green onions if desired, and serve hot.</li>
    </ol>

