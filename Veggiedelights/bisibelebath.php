    <p><strong>Spiced rice and lentil porridge, rich and tangy, simmered with tamarind and aromatic spices.</strong></p>

    <h2>Ingredients</h2>
    <ul>
      <li>1 cup rice</li>
      <li>½ cup toor dal</li>
      <li>1 tbsp tamarind pulp</li>
      <li>1 tbsp bisi bele bath powder</li>
      <li>1 cup mixed vegetables (carrot, beans, peas)</li>
      <li>2 tbsp ghee</li>
      <li>Salt to taste</li>
      <li>Cashews for garnish</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Cook rice and dal together until soft.</li>
      <li>Boil vegetables separately until tender.</li>
      <li>In a pan, heat ghee, add bisi bele bath powder and tamarind pulp.</li>
      <li>Mix in cooked rice-dal mixture and vegetables, add salt and simmer for 5–10 minutes.</li>
      <li>Garnish with fried cashews and serve hot with boondi or papad.</li>
    </ol>
