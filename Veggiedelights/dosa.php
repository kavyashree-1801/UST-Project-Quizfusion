  <div class="recipe-content">
    <p><strong>Crispy rice and lentil crepe, served with sambar and chutney.</strong></p>

    <h2>Ingredients</h2>
    <ul>
      <li>1 cup urad dal</li>
      <li>2 cups raw rice</li>
      <li>½ cup poha (flattened rice)</li>
      <li>Salt to taste</li>
      <li>Oil for cooking</li>
      <li>Water as needed</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Soak urad dal, rice, and poha separately for 4–6 hours.</li>
      <li>Grind each with water into smooth batter, combine, and ferment overnight.</li>
      <li>Add salt and mix well before use.</li>
      <li>Pour a ladle of batter onto a hot tawa and spread thin.</li>
      <li>Drizzle oil, cook till golden, and serve with chutney and sambar.</li>
    </ol>
