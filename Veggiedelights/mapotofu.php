    <p>A classic Sichuan dish featuring soft tofu in a spicy, flavorful sauce made with vegetarian-friendly chili bean paste, perfect with steamed rice.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>400g soft tofu, cut into cubes</li>
      <li>2 tbsp vegetable oil</li>
      <li>2 cloves garlic, minced</li>
      <li>1-inch ginger, minced</li>
      <li>2 tbsp vegetarian chili bean paste (doubanjiang)</li>
      <li>1 tsp soy sauce</li>
      <li>1/2 cup vegetable broth</li>
      <li>1 tsp cornstarch mixed with 2 tsp water (optional)</li>
      <li>Chopped green onions for garnish</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Heat oil in a pan over medium heat. Sauté garlic and ginger until fragrant.</li>
      <li>Add chili bean paste and cook 1–2 minutes until aromatic.</li>
      <li>Gently add tofu cubes and pour in vegetable broth and soy sauce.</li>
      <li>Simmer 5 minutes, stirring gently to avoid breaking the tofu.</li>
      <li>If desired, add cornstarch mixture to thicken the sauce.</li>
      <li>Garnish with chopped green onions and serve hot with steamed rice.</li>
    </ol>

