    <h1>Vegetable Spring Rolls</h1>
    <img src="https://media.istockphoto.com/id/486940812/photo/baked-spring-rolls-with-deep-vegetables-and-rice.jpg?s=612x612&w=0&k=20&c=rQ5NCxvLHt8zhO3oXFCU8QCfoVG_94jKTMWjti05Bso=" alt="Vegetable Spring Rolls">
    <p>Crispy spring rolls filled with a mix of sautéed vegetables, perfect as an appetizer or snack.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>6–8 spring roll wrappers</li>
      <li>1 cup cabbage, shredded</li>
      <li>1/2 cup carrot, julienned</li>
      <li>1/2 cup bell peppers, sliced</li>
      <li>2 cloves garlic, minced</li>
      <li>1 tsp soy sauce</li>
      <li>Oil for frying</li>
      <li>Salt and pepper to taste</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Heat a little oil in a pan. Sauté garlic and vegetables until tender.</li>
      <li>Add soy sauce, salt, and pepper. Mix well and let it cool slightly.</li>
      <li>Place a portion of the filling on a spring roll wrapper and roll tightly.</li>
      <li>Heat oil in a pan and deep-fry the rolls until golden brown and crispy.</li>
      <li>Drain on paper towels and serve hot with sweet chili sauce.</li>
    </ol>

