    <h1>Kesari Bath</h1>
    <img src="https://images.archanaskitchen.com/images/recipes/indian/sweet-recipes/Pineapple_Kesari_Bhath_9e29000d52.jpg" alt="Kesari Bath">
    <p>Sweet semolina dessert flavored with saffron, cardamom, and ghee, a South Indian delicacy.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>1 cup rava/semolina</li>
      <li>3/4 cup sugar</li>
      <li>3 cups water</li>
      <li>2 tbsp ghee</li>
      <li>1/4 tsp saffron strands</li>
      <li>2-3 cardamom pods, powdered</li>
      <li>A few cashews and raisins</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Heat ghee in a pan and roast semolina until aromatic.</li>
      <li>Add water gradually and stir to avoid lumps.</li>
      <li>Add sugar, saffron, and cardamom powder. Cook until thick.</li>
      <li>Garnish with fried cashews and raisins.</li>
      <li>Serve warm.</li>
    </ol>

