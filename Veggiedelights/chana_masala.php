    <h1>Chana Masala</h1>
    <img src="https://www.indianhealthyrecipes.com/wp-content/uploads/2021/08/chana-masala-recipe.jpg" alt="Chana Masala">
    <p>Spiced chickpeas cooked in tangy tomato gravy, a North Indian favorite served with rice or roti.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>1 cup chickpeas (soaked overnight)</li>
      <li>2 onions, chopped</li>
      <li>2 tomatoes, pureed</li>
      <li>1 tsp ginger-garlic paste</li>
      <li>1 tsp cumin seeds</li>
      <li>1 tsp garam masala</li>
      <li>1/2 tsp red chili powder</li>
      <li>2 tbsp oil</li>
      <li>Salt to taste</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Cook soaked chickpeas until soft.</li>
      <li>Heat oil, sauté cumin and onions till golden.</li>
      <li>Add ginger-garlic paste and tomato puree, cook 5 minutes.</li>
      <li>Add spices and cooked chickpeas. Simmer 10–15 minutes.</li>
      <li>Serve hot with rice or roti.</li>
    </ol>

