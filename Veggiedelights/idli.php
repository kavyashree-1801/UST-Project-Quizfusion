    <p><strong>Steamed rice cakes, soft and fluffy, served with chutney.</strong></p>
    
    <h2>Ingredients:</h2>
    <ul>
      <li>1 cup urad dal (split black gram)</li>
      <li>2 cups idli rice or parboiled rice</li>
      <li>1 teaspoon fenugreek seeds</li>
      <li>Salt to taste</li>
      <li>Water, as needed</li>
      <li>Oil, for greasing idli molds</li>
    </ul>

    <h2>Instructions:</h2>
    <ol>
      <li>Wash and soak urad dal and fenugreek seeds in water for 4–6 hours.</li>
      <li>Wash and soak rice separately for the same duration.</li>
      <li>Grind urad dal into a smooth, fluffy batter, adding water as needed.</li>
      <li>Grind rice into a slightly coarse batter and mix both together.</li>
      <li>Add salt and allow the batter to ferment overnight or for 8–10 hours until it doubles in volume.</li>
      <li>Grease idli molds and pour batter into them.</li>
      <li>Steam for 10–12 minutes until soft and cooked through.</li>
      <li>Serve hot with coconut chutney and sambar.</li>
    </ol>
