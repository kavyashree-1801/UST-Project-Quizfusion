    <h1>Aloo Gobi</h1>
    <img src="https://niksharmacooks.com/wp-content/uploads/2022/11/AlooGobiDSC_5234.jpg" alt="Aloo Gobi">
    <p>Classic Indian curry of potatoes and cauliflower cooked with spices, tomato, and herbs.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>2 cups cauliflower florets</li>
      <li>2 cups diced potatoes</li>
      <li>1 onion, chopped</li>
      <li>2 tomatoes, chopped</li>
      <li>1 tsp ginger-garlic paste</li>
      <li>1 tsp cumin seeds</li>
      <li>1 tsp turmeric powder</li>
      <li>1 tsp red chili powder</li>
      <li>1 tsp garam masala</li>
      <li>2 tbsp oil</li>
      <li>Salt to taste</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Heat oil, sauté cumin seeds and onions until golden.</li>
      <li>Add ginger-garlic paste and tomatoes, cook until soft.</li>
      <li>Add turmeric, chili powder, salt, cauliflower, and potatoes. Mix well.</li>
      <li>Cover and cook until vegetables are tender.</li>
      <li>Sprinkle garam masala and serve hot with roti or rice.</li>
    </ol>

