    <h1>Paneer Butter Masala</h1>
    <img src="https://aartimadan.com/wp-content/uploads/2023/11/Paneer-Butter-Masala-Restaurant-Style.jpg" alt="Paneer Butter Masala">
    <p>A rich and creamy curry made with paneer cubes simmered in a buttery tomato gravy, flavored with spices and a hint of cream.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>200g paneer (cubed)</li>
      <li>2 tbsp butter</li>
      <li>1 onion (chopped)</li>
      <li>2 tomatoes (pureed)</li>
      <li>1 tsp ginger-garlic paste</li>
      <li>½ tsp red chili powder</li>
      <li>½ tsp garam masala</li>
      <li>¼ cup fresh cream</li>
      <li>Salt to taste</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Heat butter in a pan, add onions and sauté till golden.</li>
      <li>Add ginger-garlic paste and tomato puree; cook till the oil separates.</li>
      <li>Mix in spices and salt, then add paneer cubes.</li>
      <li>Pour in cream, simmer for 3–4 minutes.</li>
      <li>Serve hot with naan or jeera rice.</li>
    </ol>

