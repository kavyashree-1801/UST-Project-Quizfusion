    <p>Comforting North Indian dish of red kidney beans cooked in spicy tomato gravy, served with steamed rice.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>1 cup rajma (red kidney beans)</li>
      <li>2 onions (chopped)</li>
      <li>2 tomatoes (pureed)</li>
      <li>1 tsp ginger-garlic paste</li>
      <li>1 tsp cumin seeds</li>
      <li>1 tsp garam masala</li>
      <li>½ tsp red chili powder</li>
      <li>2 tbsp oil</li>
      <li>Salt to taste</li>
      <li>Cooked rice for serving</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Soak rajma overnight and pressure cook until soft.</li>
      <li>Sauté cumin seeds and onions in oil till golden.</li>
      <li>Add ginger-garlic paste and tomato puree; cook well.</li>
      <li>Mix in spices, salt, and cooked rajma. Simmer for 10–15 minutes.</li>
      <li>Serve hot with rice.</li>
    </ol>

