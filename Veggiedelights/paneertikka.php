    <h1>Paneer Tikka</h1>
    <img src="https://spicecravings.com/wp-content/uploads/2020/10/Paneer-Tikka-Featured-1.jpg" alt="Paneer Tikka">
    <p>Chunks of marinated paneer grilled with vegetables, smoky and flavorful — a perfect North Indian starter.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>200g paneer (cubed)</li>
      <li>½ cup thick curd</li>
      <li>1 tsp ginger-garlic paste</li>
      <li>½ tsp turmeric powder</li>
      <li>1 tsp red chili powder</li>
      <li>1 tsp garam masala</li>
      <li>1 tbsp lemon juice</li>
      <li>Salt to taste</li>
      <li>Capsicum, onion, tomato (cubed)</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Mix curd with spices, salt, lemon juice, and ginger-garlic paste.</li>
      <li>Add paneer and veggies; marinate for 30 minutes.</li>
      <li>Thread onto skewers and grill or pan-fry till golden.</li>
      <li>Serve hot with mint chutney and lemon wedges.</li>
    </ol>

