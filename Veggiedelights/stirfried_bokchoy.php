    <p>Fresh bok choy stir-fried with garlic and a light soy sauce, simple and healthy Chinese dish.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>2 cups bok choy, chopped</li>
      <li>2 garlic cloves, minced</li>
      <li>1 tbsp soy sauce</li>
      <li>1 tsp sesame oil</li>
      <li>Salt to taste</li>
      <li>1 tbsp vegetable oil</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Heat oil in a pan. Add garlic and sauté until fragrant.</li>
      <li>Add chopped bok choy and stir-fry for 3-4 minutes.</li>
      <li>Add soy sauce, sesame oil, and salt. Toss well.</li>
      <li>Cook for another 2 minutes until tender-crisp.</li>
      <li>Serve immediately as a side or main dish.</li>
    </ol>

