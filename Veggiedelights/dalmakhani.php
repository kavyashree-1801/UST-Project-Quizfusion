    <h1>Dal Makhani</h1>
    <img src="https://www.sharmispassions.com/wp-content/uploads/2012/05/dal-makhani7-500x500.jpg" alt="Dal Makhani">
    <p>Slow-cooked whole black lentils and kidney beans in a creamy, buttery sauce for a luxurious North Indian delicacy.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>½ cup whole black urad dal</li>
      <li>2 tbsp rajma</li>
      <li>2 tomatoes (pureed)</li>
      <li>1 onion (chopped)</li>
      <li>1 tbsp butter + 1 tbsp cream</li>
      <li>1 tsp ginger-garlic paste</li>
      <li>½ tsp garam masala</li>
      <li>½ tsp red chili powder</li>
      <li>Salt to taste</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Soak dal and rajma overnight, then pressure cook until soft.</li>
      <li>Sauté onion and ginger-garlic paste in butter till golden.</li>
      <li>Add tomato puree, spices, and salt. Cook till oil separates.</li>
      <li>Add cooked dal-rajma mixture and simmer for 20 minutes.</li>
      <li>Stir in cream and serve with naan or rice.</li>
    </ol>

