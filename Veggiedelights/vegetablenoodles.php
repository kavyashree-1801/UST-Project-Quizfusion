    <h1>Vegetable Noodles</h1>
    <img src="https://shwetainthekitchen.com/wp-content/uploads/2023/03/vegetable-noodles.jpg" alt="Vegetable Noodles">
    <p>Stir-fried noodles tossed with a colorful mix of fresh vegetables, flavored with soy sauce and garlic for a quick and tasty meal.</p>

    <h2>Ingredients</h2>
    <ul>
      <li>200g noodles (egg-free for vegetarian)</li>
      <li>1 cup cabbage, shredded</li>
      <li>1/2 cup carrot, julienned</li>
      <li>1/2 cup bell peppers, sliced</li>
      <li>2 cloves garlic, minced</li>
      <li>2 tbsp soy sauce</li>
      <li>1 tbsp sesame oil</li>
      <li>1/2 tsp black pepper</li>
    </ul>

    <h2>Instructions</h2>
    <ol>
      <li>Cook noodles according to package instructions. Drain and set aside.</li>
      <li>Heat sesame oil in a pan. Sauté garlic until fragrant.</li>
      <li>Add cabbage, carrot, and bell peppers. Stir-fry 3–4 minutes until slightly tender.</li>
      <li>Add cooked noodles and soy sauce. Toss well to combine.</li>
      <li>Sprinkle black pepper and serve hot.</li>
    </ol>

